# Function FindFirstDigit(StrPtr):int
:FindFirstDigit
@PUSHRETURN
=StrPtr Var01
@PUSHLOCALI StrPtr
@POPI StrPtr


@PUSHII StrPtr @AND 0xff
@WHILE_NOTZERO
   @IF_GE_A "0\0"
      @IF_LE_A "9\0"
         # Found digit return value
         @SUB "0\0"
         @POPLOCAL StrPtr
         @POPRETURN
         @RET
      @ENDIF
   @ENDIF
   @POPNULL
   @INCI StrPtr
   @PUSHII StrPtr @AND 0xff
@ENDWHILE
@PRTLN "Error, Failed to find any digits in string"
@POPNULL
@PUSH 0
@POPLOCAL StrPtr
@POPRETURN
@RET
